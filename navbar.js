window.activatePanic = function() {
    const panicSettings = JSON.parse(localStorage.getItem('panicKey') || '{}');
    if (panicSettings.url) {
        window.location.href = panicSettings.url;
    } else {
        alert('Please set up a panic URL in settings first');
    }
};

